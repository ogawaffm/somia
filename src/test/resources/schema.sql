DROP TABLE IF EXISTS ALL_DATA_TYPES;
CREATE TABLE ALL_DATA_TYPES (
    ID INTEGER PRIMARY KEY,

    /**************************************************************************/
    /* String Data Types                                                      */
    /**************************************************************************/

    // Character  String Types

    -- with default size of 1 character
    CHAR_COL CHAR,
    CHAR_VARYING_COL CHAR VARYING,
    CHARACTER_COL CHARACTER,
    CHARACTER_VARYING_COL CHARACTER VARYING,
    VARCHAR_COL VARCHAR,
    VARCHAR_CASESENSITIVE_COL VARCHAR_CASESENSITIVE,
    VARCHAR_IGNORECASE_COL VARCHAR_IGNORECASE,
    NCHAR_COL NCHAR,
    NCHAR_VARYING_COL NCHAR VARYING,
    NATIONAL_CHAR_VARYING_COL NATIONAL CHAR VARYING,
    NATIONAL_CHARACTER_VARYING_COL NATIONAL CHARACTER VARYING,

    -- with size of 1 character
    CHAR_1_COL CHAR(1),
    CHAR_VARYING_1_COL CHAR VARYING(1),
    CHARACTER_1_COL CHARACTER(1),
    CHARACTER_VARYING_1_COL CHARACTER VARYING(1),
    VARCHAR_1_COL VARCHAR(1),
    VARCHAR_CASESENSITIVE_1_COL VARCHAR_CASESENSITIVE(1),
    VARCHAR_IGNORECASE_1_COL VARCHAR_IGNORECASE(1),
    NCHAR_1_COL NCHAR(1),
    NCHAR_VARYING_1_COL NCHAR VARYING(1),
    NATIONAL_CHAR_VARYING_1_COL NATIONAL CHAR VARYING(1),
    NATIONAL_CHARACTER_VARYING_1_COL NATIONAL CHARACTER VARYING(1),

    -- with maximum size of 1000000000 characters
    CHAR_1000000000_COL CHAR(1000000000),
    CHAR_VARYING_1000000000_COL CHAR VARYING(1000000000),
    CHARACTER_1000000000_COL CHARACTER(1000000000),
    CHARACTER_VARYING_1000000000_COL CHARACTER VARYING(1000000000),
    VARCHAR_1000000000_COL VARCHAR(1000000000),
    VARCHAR_CASESENSITIVE_1000000000_COL VARCHAR_CASESENSITIVE(1000000000),
    VARCHAR_IGNORECASE_1000000000_COL VARCHAR_IGNORECASE(1000000000),
    NCHAR_1000000000_COL NCHAR(1000000000),
    NCHAR_VARYING_1000000000_COL NCHAR VARYING(1000000000),
    NATIONAL_CHAR_VARYING_1000000000_COL NATIONAL CHAR VARYING(1000000000),
    NATIONAL_CHARACTER_VARYING_1000000000_COL NATIONAL CHARACTER VARYING(1000000000),

    -- with default size of 1 character
    CLOB_COL CLOB,
    CHAR_LARGE_OBJECT_COL CHAR LARGE OBJECT,
    CHARACTER_LARGE_OBJECT_COL CHARACTER LARGE OBJECT,
    NATIONAL_CHARACTER_LARGE_OBJECT_COL NATIONAL CHARACTER LARGE OBJECT,
    NCLOB_COL NCLOB,

    -- with size of 1 character
    CLOB_1_COL CLOB(1),
    CHAR_LARGE_OBJECT_1_COL CHAR LARGE OBJECT(1),
    CHARACTER_LARGE_OBJECT_1_COL CHARACTER LARGE OBJECT(1),
    NATIONAL_CHARACTER_LARGE_OBJECT_1_COL NATIONAL CHARACTER LARGE OBJECT(1),
    NCLOB_1_COL NCLOB(1),

    -- Binary String Types

    -- with default size of 1 byte
    BINARY_COL BINARY,
    BINARY_VARYING_COL BINARY VARYING,
    VARBINARY_COL VARBINARY,

    -- with size of 1 byte
    BINARY_1_COL BINARY(1),
    BINARY_VARYING_1_COL BINARY VARYING(1),
    VARBINARY_1_COL VARBINARY(1),

    -- with maximum size of 1000000000 bytes
    BINARY_1000000000_COL BINARY(1000000000),
    BINARY_VARYING_1000000000_COL BINARY VARYING(1000000000),
    VARBINARY_1000000000_COL VARBINARY(1000000000),

    -- with default size of 1 byte
    BLOB_COL BLOB,
    BINARY_LARGE_OBJECT_COL BINARY LARGE OBJECT,

    -- with default size of 1 byte
    BLOB_1_COL BLOB(1),
    BINARY_LARGE_OBJECT_1_COL BINARY LARGE OBJECT(1),

    -- with maximum size of 1 k bytes
    BLOB_1K_COL BLOB(1 K),
    BINARY_LARGE_OBJECT_1K_COL BINARY LARGE OBJECT(1 K),

    -- with maximum size of 1000000000 bytes
    BLOB_1000000000_COL BLOB(1000000000),
    BINARY_LARGE_OBJECT_1000000000_COL BINARY LARGE OBJECT(1000000000),

    BOOLEAN_COL BOOLEAN,

    TINYINT_COL TINYINT,
    SMALLINT_COL SMALLINT,
    INT_COL INT,
    INTEGER_COL INTEGER,
    BIGINT_COL BIGINT,

    REAL_COL REAL,

    FLOAT_COL FLOAT,
    FLOAT_1_COL FLOAT(1),
    FLOAT_24_COL FLOAT(24),
    FLOAT_25_COL FLOAT(25),
    FLOAT_53_COL FLOAT(53),

    DOUBLE_COL DOUBLE,
    DOUBLE_PRECISION_COL DOUBLE PRECISION,

    DECFLOAT_COL DECFLOAT,
    DECFLOAT_1_COL DECFLOAT(1),
    DECFLOAT_100000_COL DECFLOAT(100000),

    DEC_COL DEC,
    DECIMAL_COL DECIMAL,
    NUMERIC_COL NUMERIC,

    DEC_1_COL DEC(1),
    DECIMAL_1_COL DECIMAL(1),
    NUMERIC_1_COL NUMERIC(1),

    DEC_1_0_COL DEC(1, 0),
    DECIMAL_1_0_COL DECIMAL(1, 0),
    NUMERIC_1_0_COL NUMERIC(1, 0),

    DEC_1_1_COL DEC(1, 1),
    DECIMAL_1_1_COL DECIMAL(1, 1),
    NUMERIC_1_1_COL NUMERIC(1, 1),

    DEC_100000_COL DEC(100000),
    DECIMAL_100000_COL DECIMAL(100000),
    NUMERIC_100000_COL NUMERIC(100000),

    DEC_100000_0_COL DEC(100000, 0),
    DECIMAL_100000_0_COL DECIMAL(100000, 0),
    NUMERIC_100000_0_COL NUMERIC(100000, 0),

    DEC_100000_1_COL DEC(100000, 1),
    DECIMAL_100000_1_COL DECIMAL(100000, 1),
    NUMERIC_100000_1_COL NUMERIC(100000, 1),

    DATE_COL DATE,

    TIME_COL TIME,
    TIME_0_COL TIME(0),
    TIME_1_COL TIME(1),
    TIME_9_COL TIME(9),

    TIME_WOTZ_COL TIME WITHOUT TIME ZONE,
    TIME_0_WOTZ_COL TIME(0) WITHOUT TIME ZONE,
    TIME_1_WOTZ_COL TIME(1) WITHOUT TIME ZONE,
    TIME_9_WOTZ_COL TIME(9) WITHOUT TIME ZONE,

    TIME_WTZ_COL TIME WITH TIME ZONE,
    TIME_0_WTZ_COL TIME(0) WITH TIME ZONE,
    TIME_1_WTZ_COL TIME(1) WITH TIME ZONE,
    TIME_9_WTZ_COL TIME(9) WITH TIME ZONE,

    TIMESTAMP_COL TIMESTAMP,
    TIMESTAMP_0_COL TIMESTAMP(0),
    TIMESTAMP_1_COL TIMESTAMP(1),
    TIMESTAMP_9_COL TIMESTAMP(9),

    UUID_COL UUID

                            )